  static Map<String, String> forStory(String storyId, {required String lang}) {
    String id = storyId.toLowerCase();
    // Build a slug version: replace non-alphanumerics with '-', collapse repeats, trim leading/trailing '-'
    final slug = id
        .replaceAll(RegExp(r'[^a-z0-9]+'), '-')
        .replaceAll(RegExp(r'-+'), '-')
        .replaceAll(RegExp(r'^-'), '')
        .replaceAll(RegExp(r'-$'), '');

    Map<String, Map<String, String>>? story = data[id] ?? data[slug];

    if (story == null) return const {};
    final map = story[lang] ?? story['en'] ?? const {};
    return Map<String, String>.from(map);
  }
